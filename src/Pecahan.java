
public class Pecahan {
	int atas,bawah;
	
	Pecahan(int atas,int bawah){
		this.atas=atas;
		this.bawah=bawah;
	}
	
	int getAtas() {
		return this.atas;
	}
	
	int getBawah() {
		return this.bawah;
	}
	
	void setAtas(int value) {
		this.atas=value;
	}
	
	void setBawah(int value) {
		this.bawah=value;
	}
}
